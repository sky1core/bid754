// tools/codegen/main.go - Generate Go/Rust type/constant code from the symbol registry
//
// Reads tools/registry/symbols.json and generates:
//   --target=go    → bid754 root gen_*_check.go files
//   --target=rust  → bid754-rs/src/gen_*.rs files
//   --target=check → validates registry against existing code (no writes)
//
// Usage:
//   go run tools/codegen/main.go --target=rust
//   go run tools/codegen/main.go --target=go
//   go run tools/codegen/main.go --target=check

package main

import (
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strconv"
	"strings"
)

// Same types as symextract
type Registry struct {
	Version    string              `json:"version"`
	Source     string              `json:"source"`
	Types      map[string]TypeDef  `json:"types"`
	Constants  map[string]ConstDef `json:"constants"`
	Tables     map[string]TableDef `json:"tables"`
	Functions  map[string]FuncDef  `json:"functions"`
	ConstGroup map[string][]string `json:"const_groups"`
}

type TypeDef struct {
	Fields   []FieldDef `json:"fields"`
	AliasOf  string     `json:"alias_of,omitempty"`
	GoFile   string     `json:"go_file"`
	Exported bool       `json:"exported"`
}

type FieldDef struct {
	Name string `json:"name"`
	Type string `json:"type"`
}

type ConstDef struct {
	Value    string `json:"value"`
	Type     string `json:"type"`
	GoFile   string `json:"go_file"`
	Group    string `json:"group,omitempty"`
	AliasOf  string `json:"alias_of,omitempty"`
	Exported bool   `json:"exported"`
}

type TableDef struct {
	ElementType string `json:"element_type"`
	Length      int    `json:"length"`
	GoFile      string `json:"go_file"`
	Exported    bool   `json:"exported"`
	IsSlice     bool   `json:"is_slice,omitempty"`
}

type FuncDef struct {
	Params   []ParamDef `json:"params"`
	Returns  []string   `json:"returns"`
	GoFile   string     `json:"go_file"`
	GoName   string     `json:"go_name"`
	Exported bool       `json:"exported"`
	Group    string     `json:"group,omitempty"`
}

type ParamDef struct {
	Name    string `json:"name"`
	Type    string `json:"type"`
	Pointer bool   `json:"pointer,omitempty"`
}

var target = flag.String("target", "check", "Generation target: go, rust, readtest-rust, or check")

func main() {
	flag.Parse()

	projectRoot := findProjectRoot()
	regPath := filepath.Join(projectRoot, "tools", "registry", "symbols.json")

	reg := loadRegistry(regPath)

	switch *target {
	case "rust":
		generateRust(reg, projectRoot)
	case "readtest-rust":
		generateReadtestRust(projectRoot)
	case "go":
		generateGo(reg, projectRoot)
	case "check":
		checkRegistry(reg)
	default:
		fmt.Fprintf(os.Stderr, "unknown target: %s\n", *target)
		os.Exit(1)
	}
}

func findProjectRoot() string {
	dir, _ := os.Getwd()
	for {
		if _, err := os.Stat(filepath.Join(dir, "go.mod")); err == nil {
			return dir
		}
		parent := filepath.Dir(dir)
		if parent == dir {
			fmt.Fprintln(os.Stderr, "cannot find project root")
			os.Exit(1)
		}
		dir = parent
	}
}

func loadRegistry(path string) *Registry {
	data, err := os.ReadFile(path)
	if err != nil {
		fmt.Fprintf(os.Stderr, "read registry: %v\n", err)
		os.Exit(1)
	}
	var reg Registry
	if err := json.Unmarshal(data, &reg); err != nil {
		fmt.Fprintf(os.Stderr, "parse registry: %v\n", err)
		os.Exit(1)
	}
	return &reg
}

// ==================== Rust Generation ====================

func generateRust(reg *Registry, projectRoot string) {
	outDir := filepath.Join(projectRoot, "bid754-rs", "src")

	// 1. Generate types.rs
	genRustTypes(reg, outDir)

	// 2. Generate constants.rs
	genRustConstants(reg, outDir)

	// The src/generated/ tree (including prelude.rs) is owned exclusively by
	// tools/go2rs; this target must not write into it.

	fmt.Println("Rust generation complete")
}

func genRustTypes(reg *Registry, outDir string) {
	var sb strings.Builder
	sb.WriteString("// Code generated by tools/codegen. DO NOT EDIT.\n")
	sb.WriteString("// Source: tools/registry/symbols.json\n\n")

	// Sort type names
	names := sortedKeys(reg.Types)

	for _, name := range names {
		t := reg.Types[name]
		if t.AliasOf != "" {
			rustType, ok := normalizeRustType(t.AliasOf)
			if !ok {
				sb.WriteString(fmt.Sprintf("// skipped alias %s = %s (unsupported Rust target type)\n\n", name, t.AliasOf))
				continue
			}
			sb.WriteString(fmt.Sprintf("pub type %s = %s;\n\n", name, rustType))
			continue
		}
		if typeHasUnsupportedRustFields(t) {
			sb.WriteString(fmt.Sprintf("// skipped struct %s (contains unsupported Rust target field types)\n\n", name))
			continue
		}
		sb.WriteString(fmt.Sprintf("#[derive(Clone, Copy, Default, Debug, PartialEq, Eq)]\n"))
		sb.WriteString(fmt.Sprintf("#[repr(C)]\n"))
		sb.WriteString(fmt.Sprintf("pub struct %s {\n", name))
		for _, f := range t.Fields {
			rustType, ok := normalizeRustType(f.Type)
			if !ok {
				continue
			}
			sb.WriteString(fmt.Sprintf("    pub %s: %s,\n", f.Name, rustType))
		}
		sb.WriteString("}\n\n")
	}

	path := filepath.Join(outDir, "gen_types.rs")
	writeFile(path, sb.String())
	fmt.Printf("  gen_types.rs: %d types\n", len(reg.Types))
}

func typeHasUnsupportedRustFields(t TypeDef) bool {
	for _, f := range t.Fields {
		if fieldHasUnsupportedRustType(f.Type) {
			return true
		}
	}
	return false
}

func fieldHasUnsupportedRustType(typ string) bool {
	return strings.Contains(typ, "Int") || strings.Contains(typ, "&mut")
}

func genRustConstants(reg *Registry, outDir string) {
	var sb strings.Builder
	sb.WriteString("// Code generated by tools/codegen. DO NOT EDIT.\n")
	sb.WriteString("// Source: tools/registry/symbols.json\n\n")
	sb.WriteString("use super::gen_types::*;\n\n")

	// Group constants
	groups := []string{"rounding", "flags", "internal", "bid64", "bid128", "bid32", "misc"}
	emitted := make(map[string]bool)

	for _, group := range groups {
		names, ok := reg.ConstGroup[group]
		if !ok || len(names) == 0 {
			continue
		}

		sb.WriteString(fmt.Sprintf("// === %s ===\n", group))

		sort.Strings(names)
		for _, name := range names {
			c := reg.Constants[name]
			if emitted[name] {
				continue
			}
			emitted[name] = true

			rustType, ok := normalizeRustConstType(c.Type)
			if !ok {
				continue
			}
			if rustType == "" {
				rustType = "u64"
			}

			value := c.Value
			if value == "" {
				value = "0"
			}

			// Handle alias
			if c.AliasOf != "" {
				sb.WriteString(fmt.Sprintf("pub const %s: %s = %s;\n", name, rustType, c.AliasOf))
			} else {
				// Fix: ensure hex values have proper Rust suffix
				sb.WriteString(fmt.Sprintf("pub const %s: %s = %s;\n", name, rustType, rustLiteral(value, rustType)))
			}
		}
		sb.WriteString("\n")
	}

	path := filepath.Join(outDir, "gen_constants.rs")
	writeFile(path, sb.String())
	fmt.Printf("  gen_constants.rs: %d constants\n", len(reg.Constants))
}

// ==================== Go Generation ====================

func generateGo(reg *Registry, projectRoot string) {
	outDir := projectRoot

	// Generate gen_types.go (for validation - not replacing existing yet)
	genGoTypes(reg, outDir)

	// Generate gen_constants.go
	genGoConstants(reg, outDir)

	fmt.Println("Go generation complete")
}

func genGoTypes(reg *Registry, outDir string) {
	var sb strings.Builder
	sb.WriteString("// Code generated by tools/codegen. DO NOT EDIT.\n")
	sb.WriteString("// Source: tools/registry/symbols.json\n\n")
	sb.WriteString("package bid754\n\n")

	names := sortedKeys(reg.Types)
	for _, name := range names {
		t := reg.Types[name]
		if t.AliasOf != "" {
			sb.WriteString(fmt.Sprintf("// type %s = %s\n\n", name, rustTypeToGo(t.AliasOf)))
			continue
		}
		if typeHasUnsupportedRustFields(t) {
			sb.WriteString(fmt.Sprintf("// %s: skipped (contains non-portable types)\n\n", name))
			continue
		}

		sb.WriteString(fmt.Sprintf("// type %s struct {\n", name))
		for _, f := range t.Fields {
			goType := rustTypeToGo(f.Type)
			sb.WriteString(fmt.Sprintf("//     %s %s\n", f.Name, goType))
		}
		sb.WriteString("// }\n\n")
	}

	path := filepath.Join(outDir, "gen_types_check.go")
	writeFile(path, sb.String())
	fmt.Printf("  gen_types_check.go: %d types (validation only)\n", len(reg.Types))
}

func genGoConstants(reg *Registry, outDir string) {
	var sb strings.Builder
	sb.WriteString("// Code generated by tools/codegen. DO NOT EDIT.\n")
	sb.WriteString("// Source: tools/registry/symbols.json\n")
	sb.WriteString("// This file is for validation only - compare with existing constants.\n\n")
	sb.WriteString("package bid754\n\n")

	groups := []string{"rounding", "flags", "internal", "bid64", "bid128", "bid32", "misc"}
	emitted := make(map[string]bool)

	for _, group := range groups {
		names, ok := reg.ConstGroup[group]
		if !ok || len(names) == 0 {
			continue
		}

		sb.WriteString(fmt.Sprintf("// %s\n", group))
		sb.WriteString("// const (\n")

		sort.Strings(names)
		for _, name := range names {
			c := reg.Constants[name]
			if emitted[name] {
				continue
			}
			emitted[name] = true

			goType := rustTypeToGo(c.Type)
			if goType == "" {
				goType = "uint64"
			}

			value := c.Value
			if value == "" {
				value = "0"
			}

			sb.WriteString(fmt.Sprintf("//     %s %s = %s\n", name, goType, value))
		}
		sb.WriteString("// )\n\n")
	}

	path := filepath.Join(outDir, "gen_constants_check.go")
	writeFile(path, sb.String())
	fmt.Printf("  gen_constants_check.go: %d constants (validation only)\n", len(reg.Constants))
}

// ==================== Check ====================

func checkRegistry(reg *Registry) {
	fmt.Printf("Registry v%s: %s\n", reg.Version, reg.Source)
	fmt.Printf("  Types:     %d\n", len(reg.Types))
	fmt.Printf("  Constants: %d\n", len(reg.Constants))
	fmt.Printf("  Tables:    %d\n", len(reg.Tables))
	fmt.Printf("  Functions: %d\n", len(reg.Functions))

	// Check for potential issues
	issues := 0

	// Check alias integrity
	for name, c := range reg.Constants {
		if c.AliasOf != "" {
			if _, ok := reg.Constants[c.AliasOf]; !ok {
				fmt.Printf("  WARNING: %s alias_of %s not found\n", name, c.AliasOf)
				issues++
			}
		}
	}

	// Check for value-only aliases that are likely wrong
	// (same value but different semantic group)
	for name, c := range reg.Constants {
		if c.AliasOf != "" {
			target := reg.Constants[c.AliasOf]
			if c.Group != target.Group {
				fmt.Printf("  WARNING: cross-group alias: %s (%s) -> %s (%s)\n",
					name, c.Group, c.AliasOf, target.Group)
				issues++
			}
		}
	}

	// Check tables with unknown length
	unknownLen := 0
	for _, t := range reg.Tables {
		if t.Length < 0 {
			unknownLen++
		}
	}
	if unknownLen > 0 {
		fmt.Printf("  INFO: %d tables with unknown length (slices)\n", unknownLen)
	}

	if issues > 0 {
		fmt.Printf("\n  %d issues found\n", issues)
	} else {
		fmt.Println("\n  No issues found")
	}
}

// ==================== Helpers ====================

func rustLiteral(value string, rustType string) string {
	if lit, ok := signedHexLiteral(value, rustType); ok {
		return lit
	}
	return value
}

func signedHexLiteral(value string, rustType string) (string, bool) {
	switch rustType {
	case "i8", "i16", "i32", "i64":
	default:
		return "", false
	}
	if !strings.HasPrefix(value, "0x") && !strings.HasPrefix(value, "0X") {
		return "", false
	}
	u, err := strconv.ParseUint(value[2:], 16, 64)
	if err != nil {
		return "", false
	}
	if (u & (uint64(1) << 63)) == 0 {
		return "", false
	}
	s := int64(u)
	if !fitsSignedType(s, rustType) {
		return "", false
	}
	return strconv.FormatInt(s, 10), true
}

func fitsSignedType(v int64, rustType string) bool {
	switch rustType {
	case "i8":
		return v >= -128 && v <= 127
	case "i16":
		return v >= -32768 && v <= 32767
	case "i32":
		return v >= -2147483648 && v <= 2147483647
	case "i64":
		return true
	default:
		return false
	}
}

func rustTypeToGo(rustType string) string {
	m := map[string]string{
		"u64": "uint64", "u32": "uint32", "u16": "uint16", "u8": "uint8",
		"i64": "int64", "i32": "int32", "i16": "int16", "i8": "int8",
		"f64": "float64", "f32": "float32",
		"bool": "bool", "String": "string", "&'static str": "string",
	}
	if g, ok := m[rustType]; ok {
		return g
	}
	// Handle array types [T; N] -> [N]T
	if strings.HasPrefix(rustType, "[") && strings.Contains(rustType, ";") {
		// [u64; 2] -> [2]uint64
		inner := rustType[1 : len(rustType)-1]
		parts := strings.SplitN(inner, ";", 2)
		elemType := strings.TrimSpace(parts[0])
		size := strings.TrimSpace(parts[1])
		return fmt.Sprintf("[%s]%s", size, rustTypeToGo(elemType))
	}
	return rustType
}

func sortedKeys[V any](m map[string]V) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}

func normalizeRustType(goLike string) (string, bool) {
	rustType := strings.TrimSpace(goLike)
	if rustType == "" {
		return "", false
	}
	rustType = strings.TrimPrefix(rustType, "untyped ")
	rustType = strings.ReplaceAll(rustType, "string", "String")
	if rustType == "error" {
		return "String", true
	}
	if strings.HasPrefix(rustType, "interface{") || strings.Contains(rustType, " bid754.") || strings.Contains(rustType, "bid754.") {
		return "", false
	}
	if strings.HasPrefix(rustType, "[") && strings.HasSuffix(rustType, "]byte") {
		parts := strings.SplitN(strings.TrimPrefix(rustType, "["), "]", 2)
		if len(parts) == 2 {
			return fmt.Sprintf("[u8; %s]", strings.TrimSpace(parts[0])), true
		}
	}
	return rustType, true
}

func normalizeRustConstType(goLike string) (string, bool) {
	rustType := strings.TrimSpace(goLike)
	if rustType == "" {
		return "", false
	}
	rustType = strings.TrimPrefix(rustType, "untyped ")
	switch rustType {
	case "string":
		return "&'static str", true
	case "bool":
		return "bool", true
	}
	return normalizeRustType(rustType)
}

func writeFile(path, content string) {
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		fmt.Fprintf(os.Stderr, "mkdir %s: %v\n", filepath.Dir(path), err)
		os.Exit(1)
	}
	if err := os.WriteFile(path, []byte(content), 0644); err != nil {
		fmt.Fprintf(os.Stderr, "write %s: %v\n", path, err)
		os.Exit(1)
	}
}
