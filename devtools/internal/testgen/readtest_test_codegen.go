func TestGeneratedReadCases(t *testing.T) {
	requireNative(t)
	if testing.Short() {
		t.Skip("generated readtest cases require non-short native run; use make test-native-readtest")
	}
	spec := loadGeneratedReadSpecForTest(t)
	if len(spec.ReadCases) == 0 {
		t.Fatal("expected generated read cases")
	}
	assertGeneratedReadCaseCounts(t, countGeneratedReadCases(spec.ReadCases), expectedGeneratedReadCaseCounts)

	for _, tc := range spec.ReadCases {
		tc := tc
		t.Run(tc.ID, func(t *testing.T) {
			if tc.NativeCompareSkipReason != "" {
				t.Skip(tc.NativeCompareSkipReason)
			}
			switch tc.Kind {
			case "from_string":
				got, status, err := generatedReadCaseBits(tc)
				if err != nil {
					t.Fatalf("generatedReadCaseBits(%q): %v", tc.Operands[0], err)
				}
				if strings.HasPrefix(strings.TrimSpace(tc.Expected), "[") {
					if normalizeGeneratedReadtestBits(got) != normalizeGeneratedReadtestBits(tc.Expected) {
						t.Fatalf("generated read case %s line %d: expected bits %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestBits(tc.Expected), normalizeGeneratedReadtestBits(got))
					}
				} else {
					rendered, err := generatedReadCaseRenderedDecimal(tc, got)
					if err != nil {
						t.Fatalf("generatedReadCaseRenderedDecimal(%q): %v", tc.Function, err)
					}
					if !compareDecimalResults(tc.Expected, rendered) && !areResultsEquivalent(tc.Expected, rendered) {
						t.Fatalf("generated read case %s line %d: expected %q, got %q", tc.ID, tc.Line, tc.Expected, rendered)
					}
				}
				if normalizeGeneratedReadtestStatus(status) != normalizeGeneratedReadtestStatus(tc.Status) {
					t.Fatalf("generated read case %s line %d: expected status %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestStatus(tc.Status), normalizeGeneratedReadtestStatus(status))
				}
			case "to_string":
				got, status, err := generatedReadCaseString(tc)
				if err != nil {
					t.Fatalf("generatedReadCaseString(%q): %v", tc.Operands[0], err)
				}
				if !compareDecimalResults(tc.Expected, got) && !areResultsEquivalent(tc.Expected, got) {
					t.Fatalf("generated read case %s line %d: expected %q, got %q", tc.ID, tc.Line, tc.Expected, got)
				}
				if normalizeGeneratedReadtestStatus(status) != normalizeGeneratedReadtestStatus(tc.Status) {
					t.Fatalf("generated read case %s line %d: expected status %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestStatus(tc.Status), normalizeGeneratedReadtestStatus(status))
				}
			case "binary_op", "unary_op", "ternary_op", "status_control":
				if isGeneratedReadScalarOutput(tc.OutputType) {
					got, status, err := generatedReadCaseScalarString(tc)
					if err != nil {
						t.Fatalf("generatedReadCaseScalarString(%q): %v", tc.Function, err)
					}
					if !compareGeneratedReadScalarOutput(tc.OutputType, tc.Expected, got) {
						t.Fatalf("generated read case %s line %d: expected %q, got %q", tc.ID, tc.Line, tc.Expected, got)
					}
					if normalizeGeneratedReadtestStatus(status) != normalizeGeneratedReadtestStatus(tc.Status) {
						t.Fatalf("generated read case %s line %d: expected status %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestStatus(tc.Status), normalizeGeneratedReadtestStatus(status))
					}
					return
				}
				if tc.CompareGroup == "CMP_EQUALSTATUS" {
					got, status, err := generatedReadCaseOperationString(tc)
					if err != nil {
						t.Fatalf("generatedReadCaseOperationString(%q): %v", tc.Function, err)
					}
					expected, err := generatedReadCaseExpectedString(tc)
					if err != nil {
						t.Fatalf("generatedReadCaseExpectedString(%q): %v", tc.Function, err)
					}
					if !compareDecimalResults(expected, got) && !areResultsEquivalent(expected, got) {
						t.Fatalf("generated read case %s line %d: expected numerically %q, got %q", tc.ID, tc.Line, expected, got)
					}
					if normalizeGeneratedReadtestStatus(status) != normalizeGeneratedReadtestStatus(tc.Status) {
						t.Fatalf("generated read case %s line %d: expected status %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestStatus(tc.Status), normalizeGeneratedReadtestStatus(status))
					}
					return
				}
				if strings.HasPrefix(strings.TrimSpace(tc.Expected), "[") {
					got, status, err := generatedReadCaseOperationBits(tc)
					if err != nil {
						t.Fatalf("generatedReadCaseOperationBits(%q): %v", tc.Function, err)
					}
					if normalizeGeneratedReadtestBits(got) != normalizeGeneratedReadtestBits(tc.Expected) {
						t.Fatalf("generated read case %s line %d: expected bits %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestBits(tc.Expected), normalizeGeneratedReadtestBits(got))
					}
					if normalizeGeneratedReadtestStatus(status) != normalizeGeneratedReadtestStatus(tc.Status) {
						t.Fatalf("generated read case %s line %d: expected status %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestStatus(tc.Status), normalizeGeneratedReadtestStatus(status))
					}
					return
				}
				got, status, err := generatedReadCaseOperationString(tc)
				if err != nil {
					t.Fatalf("generatedReadCaseOperationString(%q): %v", tc.Function, err)
				}
				if !compareDecimalResults(tc.Expected, got) && !areResultsEquivalent(tc.Expected, got) {
					t.Fatalf("generated read case %s line %d: expected %q, got %q", tc.ID, tc.Line, tc.Expected, got)
				}
				if normalizeGeneratedReadtestStatus(status) != normalizeGeneratedReadtestStatus(tc.Status) {
					t.Fatalf("generated read case %s line %d: expected status %q, got %q", tc.ID, tc.Line, normalizeGeneratedReadtestStatus(tc.Status), normalizeGeneratedReadtestStatus(status))
				}
			default:
				t.Fatalf("unsupported generated read kind %q", tc.Kind)
			}
		})
	}
}

