package gotypegen

import (
	"bytes"
	"fmt"
	"go/format"
	"os"
	"path/filepath"
)

func Generate(manifest Manifest) ([]byte, error) {
	var b bytes.Buffer
	b.WriteString("// Code generated by go-typegen; DO NOT EDIT.\n")
	b.WriteString("// Source manifest: typegen_manifest.json.\n\n")
	b.WriteString("package " + manifest.Package + "\n\n")

	for _, spec := range manifest.TypeAliases {
		if spec.Comment != "" {
			b.WriteString("// " + spec.Comment + "\n")
		}
		b.WriteString(fmt.Sprintf("type %s %s\n\n", spec.Name, spec.Underlying))
	}

	for _, group := range manifest.ConstGroups {
		if group.Comment != "" {
			b.WriteString("// " + group.Comment + "\n")
		}
		b.WriteString("const (\n")
		for _, value := range group.Values {
			if value.Comment != "" {
				b.WriteString(fmt.Sprintf("\t%s %s = %s // %s\n", value.Name, group.Type, value.Expr, value.Comment))
				continue
			}
			b.WriteString(fmt.Sprintf("\t%s %s = %s\n", value.Name, group.Type, value.Expr))
		}
		b.WriteString(")\n\n")
	}

	for _, spec := range manifest.ValueTypes {
		if spec.Comment != "" {
			b.WriteString("// " + spec.Comment + "\n")
		}
		b.WriteString(fmt.Sprintf("type %s %s\n\n", spec.Name, spec.Underlying))
		if spec.AccessorName != "" {
			if spec.AccessorComment != "" {
				b.WriteString("// " + spec.AccessorComment + "\n")
			}
			b.WriteString(fmt.Sprintf("func (d %s) %s() %s {\n", spec.Name, spec.AccessorName, spec.AccessorType))
			b.WriteString(fmt.Sprintf("\treturn %s(d)\n", spec.AccessorType))
			b.WriteString("}\n\n")
		}
	}

	formatted, err := format.Source(b.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format generated go: %w", err)
	}
	return formatted, nil
}

func WriteOutput(repoRoot string, manifest Manifest, data []byte) error {
	path := filepath.Join(repoRoot, manifest.Output)
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return fmt.Errorf("create output dir for %q: %w", path, err)
	}
	if err := os.WriteFile(path, data, 0o644); err != nil {
		return fmt.Errorf("write %q: %w", path, err)
	}
	return nil
}
